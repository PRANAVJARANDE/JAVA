import java.util.*;
public class Main
{
	public static void main(String[] args) 
	{
	   Scanner sc= new Scanner(System.in);
	   System.out.print("ENTER INTEGER-");
	   int a=sc.nextInt();
	   
	   System.out.print("ENTER FLOAT VALUE-");
	   float b=sc.nextFloat();
	   
	   System.out.print("ENTER BYTE-");
	   byte c=sc.nextByte();
	   
	   System.out.print("ENTER SHORT-");
	   short d=sc.nextShort();
	   
	   System.out.print("ENTER BOOLEAN-");
	   boolean e=sc.nextBoolean();
	   
	   System.out.print("ENTER DOUBLE-");
	   double f=sc.nextDouble();
	   
	   
	   System.out.print("ENTER LINE-");
	   String g=sc.next();
	   
	   
	   System.out.print("ENTER WORD-");
	   char h=sc.next().charAt(0);
	   
	   
	   System.out.print("ENTER LONG-");
	   long i=sc.nextLong();
	   
	   
	   
	   
	   
	   System.out.print("\nINTEGER-"+a);
	   System.out.print("\nFLOAT VALUE-"+b);
	   System.out.print("\nBYTE-"+c);
	   System.out.print("\nSHORT-"+d);
	   System.out.print("\nBOOLEAN-"+e);
	   System.out.print("\nDOUBLE-"+f);
	   System.out.print("LINE-"+g);
	   System.out.print("WORD-"+h);
	   System.out.print("\nLONG-"+i);
	   
	}
}
